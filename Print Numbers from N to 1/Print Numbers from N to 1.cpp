
/* Program to Print number from 1 to N */

#include <iostream>
#include <cmath>
using namespace std;

int main()

{

    int num, counter = 10;
    cout << "Please Enter a number ! " << endl;
    cin >> num;

    // we use For loops

    for (counter = num; counter >= 1; counter--) {

        cout << counter << endl;
    }

    return 0;

    // Created by @ilyes_Trabelsi
}


